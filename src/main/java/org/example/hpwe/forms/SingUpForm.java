package org.example.hpwe.forms;

public class SingUpForm {
        private String email;
        private String password;

        public String getEmail() {
            return email;
        }

        public SingUpForm setEmail(String email) {
            this.email = email;
            return this;
        }

        public String getPassword() {
            return password;
        }

        public SingUpForm setPassword(String password) {
            this.password = password;
            return this;
        }
}


